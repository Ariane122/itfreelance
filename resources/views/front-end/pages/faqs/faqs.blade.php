                                        <i class="down flaticon-download-arrow"></i><i class="up flaticon-up-arrow"></i>
                                    </span>
                                    <div class="acc-content">
                                        <p>•	Flexibilité : permet une gestion du temps plus souple.
                                            •	Réduction des temps de trajet : améliore le bien-être des employés et leur productivité.
                                            •	Équilibre travail-vie personnelle : facilite la conciliation entre vie professionnelle et privée.
                                            •	Réduction des coûts : diminue les coûts pour l'employeur (espace de bureau) et l'employé (transport).
                                            •	Accès à un plus large vivier de talents : les entreprises peuvent recruter des candidats sans contraintes géographiques.
                                            </p>
