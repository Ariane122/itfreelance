                                    <div class="acc-content">
                                        <p>Non, le travail à distance  n'est pas adapté à tous. Certaines personnes peuvent se sentir isolées, d'autres peuvent trouver difficile de gérer leur emploi du temps. Le travail à distance peut également ne pas convenir à certains métiers qui nécessitent une présence physique (ex. : soins de santé, production, vente au détail).</p>
