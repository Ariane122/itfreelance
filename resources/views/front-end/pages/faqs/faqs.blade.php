                                    </span>
                                    <div class="acc-content">
                                        <p>•	Établir des limites : fixez des heures de travail claires et déconnectez-vous après.
                                            •	S'accorder des pauses : levez-vous régulièrement, sortez pour prendre l'air.
                                            •	Maintenir une bonne hygiène de vie : avoir une alimentation équilibrée, dormir suffisamment et pratiquer une activité physique.
                                            </p>
